package it.polimi.ingsw.controller;

/**
 * this enum represent the game phase, it can be in login, initialising or turns
 */
public enum GamePhase {

    /**
     * this attribute represent the login phase
     */
    LOGIN,

    /**
     * this attribute represent the initialising phase
     */
    INITIALISING,

    /**
     * this attribute represent the turns phase
     */
    TURNS
}
